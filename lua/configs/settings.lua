-- Configurações gerais do nvim.
vim.cmd([[
  syntax on
  set encoding=UTF-8
  set hidden
  set inccommand=split
  set nowrap
  set incsearch

  set number
  set relativenumber
  set cursorline

  set tabstop=2
  set expandtab
  set shiftwidth=2
]])

-- Tema de cores
vim.cmd[[colorscheme dracula]]  

